


class Automobiles:
    def __init__(self, make, model, mileage, price, doors):
        self.make = make
        self.model = model
        self.mileage = mileage
        self.price = price
        self.doors = doors


    def set_make(self):
        self.make


    def set_model(self):
        self.model

    def set_mileage(self):
        self.mileage

    def set_price(self):
        self.price

    def set_doors(self):
        self.doors

    def get_make(self):
        return self.make


    def get_model(self):
        return self.model
    
    def get_mileage(self):
        return self.mileage
    
    def get_price(self):
        return self.price
    
    def get_doors(self):
        return self.doors


    
